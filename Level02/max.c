int	max(int *tab, unsigned int len)
{
	int i;
	int maxim;

	if(tab == '\0')
		return (0);
	i = 0;
	while(len--)
	{
		if (tab[i] > tab[i + 1])
			maxim = tab[i];
		i++;
	}
	return(maxim);
}
